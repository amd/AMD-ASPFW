sev_status_t hmac_utest(void)
{
    sev_status_t      status = SEV_STATUS_SUCCESS;
    hmac_sha256_ctx_t ctx;
    hmac_sha256_t     hmac;
    size_t            i, j;

    for (i = 0; i < sizeof(HmacVector)/sizeof(HmacVector[0]); i++)
    {
        memset(&hmac, 0, sizeof(hmac));

        status = hmac_sha256_init(&ctx, HmacVector[i].aucKey, HmacVector[i].uiKeyLen);
        if (status != SEV_STATUS_SUCCESS)
            break;

        status = hmac_sha256_update(&ctx, HmacVector[i].aucMessage, HmacVector[i].uiMessageLen);
        if (status != SEV_STATUS_SUCCESS)
            break;

        status = hmac_sha256_final(&ctx, &hmac);
        if (status != SEV_STATUS_SUCCESS)
            break;

        for (j = 0; j < sizeof(hmac); j++)
        {
            if (HmacVector[i].aucHmac[j] != hmac.hmac[j])
            {
                status = ERR_UNKNOWN;
                goto end;
            }
        }

        // Test HMAC using CCP Direct API
        /* blilient: Commented out because the input vectors are too small.
                     Function wants a minimum of 64 bytes
        status = hmac_utest_hmac256_kat(
                HmacVector[i].aucKey, HmacVector[i].uiKeyLen,
                HmacVector[i].aucMessage, HmacVector[i].uiMessageLen,
                HmacVector[i].aucHmac);
        if (status != SEV_STATUS_SUCCESS)
            goto end; */
    }

end:
    if (status != SEV_STATUS_SUCCESS)
    {
        SEV_TRACE("hmac_utest failed...\n");
        SEV_TRACE_EX(status, 0, 0, 0);
    }
    else
    {
        SEV_TRACE("hmac_utest succeed!\n");
    }

    return status;
}
